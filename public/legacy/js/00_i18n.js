  const SUPPORTED_LANGS = { fr: true, en: true };
  let _loadingLang = null;

  function isSupportedLang(lang) {
    return !!SUPPORTED_LANGS[String(lang || "").trim()];
  }

  function getLang() {
    const forced = (window.TB_LANG_FORCE || "").trim();
    if (forced && isSupportedLang(forced)) return forced;
    try {
      const stored = localStorage.getItem(LANG_KEY);
      if (stored && isSupportedLang(stored)) return stored;
    } catch (_) {}
    return "fr";
  }

  function setLang(lang) {
    if (!isSupportedLang(lang)) return;
    try { localStorage.setItem(LANG_KEY, lang); } catch (_) {}
    window.TB_LANG = lang;
  }

  function loadLangDict(lang) {
    if (DICTS[lang]) return Promise.resolve(DICTS[lang]);
    if (lang !== "en") return Promise.resolve(DICTS.fr);
    if (_loadingLang) return _loadingLang;
    _loadingLang = new Promise((resolve, reject) => {
      try {
        const script = document.createElement("script");
        script.src = "legacy/js/00_i18n_en.js";
        script.async = true;
        script.onload = () => resolve(DICTS.en || DICTS.fr);
        script.onerror = () => reject(new Error("Dictionnaire anglais indisponible."));
        document.head.appendChild(script);
      } catch (err) {
        reject(err);
      }
    }).finally(() => { _loadingLang = null; });
    return _loadingLang;
  }

  function t(key, vars) {
    const lang = getLang();
    const dict = DICTS[lang] || DICTS.fr;
    let s = dict[key] || DICTS.fr[key] || key;
    if (vars && typeof vars === "object") {
      Object.keys(vars).forEach((k) => {
        s = s.replaceAll("{" + k + "}", String(vars[k]));
      });
    }
    return s;
  }

  // Optional: apply translations to static DOM nodes
  function applyDom(rootEl) {
    const root = rootEl || document;
    const nodes = root.querySelectorAll("[data-t]");
    nodes.forEach((el) => {
      const k = el.getAttribute("data-t");
      if (!k) return;
      // Allow HTML in some nodes
      if (el.getAttribute("data-t-html") === "1") el.innerHTML = t(k);
      else el.textContent = t(k);
    });
    root.querySelectorAll("[data-t-placeholder]").forEach((el) => {
      const k = el.getAttribute("data-t-placeholder");
      if (k) el.setAttribute("placeholder", t(k));
    });
    root.querySelectorAll("[data-t-title]").forEach((el) => {
      const k = el.getAttribute("data-t-title");
      if (k) el.setAttribute("title", t(k));
    });
  }

  window.TB_I18N = DICTS;
  window.tbRegisterI18nDict = function (lang, dict) {
    if (!isSupportedLang(lang) || !dict || typeof dict !== "object") return;
    DICTS[lang] = dict;
  };
  if (window.TB_I18N_EN) window.tbRegisterI18nDict("en", window.TB_I18N_EN);
  
  function applyLangSwitchUI() {
    try {
      const lang = getLang();
      const fr = document.getElementById("tbLangFr");
      const en = document.getElementById("tbLangEn");
      if (fr) fr.classList.toggle("is-active", lang === "fr");
      if (en) en.classList.toggle("is-active", lang === "en");
    } catch (_) {}
  }

  function bindLangSwitch() {
    try {
      const fr = document.getElementById("tbLangFr");
      const en = document.getElementById("tbLangEn");
      if (fr && !fr.__tbBound) {
        fr.__tbBound = true;
        fr.addEventListener("click", () => window.tbSetLang && window.tbSetLang("fr"));
      }
      if (en && !en.__tbBound) {
        en.__tbBound = true;
        en.addEventListener("click", () => window.tbSetLang && window.tbSetLang("en"));
      }
    } catch (_) {}
  }

  window.TB_LANG = getLang();
  window.tbT = t;
  window.tbOnLangChange = window.tbOnLangChange || [];

  window.tbSetLang = function (lang) {
    setLang(lang);
    // Re-render: safest approach in this legacy app
    loadLangDict(lang).catch((err) => {
      console.warn("[i18n] lazy dictionary load failed", err);
    }).finally(() => {
      try { if (typeof refreshAll === "function") refreshAll(); } catch (_) {}
      try { if (typeof renderAll === "function") renderAll(); } catch (_) {}
      try { applyDom(); } catch (_) {}
      try { bindLangSwitch(); } catch (_) {}
      try { applyLangSwitchUI(); } catch (_) {}
      try {
        (window.tbOnLangChange || []).forEach((fn) => {
          try { if (typeof fn === "function") fn(lang); } catch (_) {}
        });
      } catch (_) {}
    });
  };
  window.tbGetLang = getLang;
  window.tbApplyI18nDom = applyDom;
  // Bind global language switch (if present)
  try { bindLangSwitch(); } catch (_) {}
  try { applyLangSwitchUI(); } catch (_) {}
